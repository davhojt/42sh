#include "shell.h"

bool	sh_command_empty(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (TRUE);
	while (str[i] && (str[i] == ' ' || str[i] == '\t'))
		i++;
	return (str[i] == 0);
}
