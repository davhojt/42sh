#include "shell.h"

void	lexer_is_quote(char *s, int i, char *dq, char *sq)
{
	if (s[i] == '"' && i && s[i - 1] == '=' && !(*dq || *sq))
		*dq = 2;
	else if (s[i] == '\'' && i && s[i - 1] == '=' && !(*dq || *sq))
		*sq = 2;
	if (s[i] == '"' && !(*dq || *sq))
		*dq = 1;
	else if (s[i] == '\'' && !(*dq || *sq))
		*sq = 1;
	else if (s[i] == '"' && (*dq || *sq))
		*dq = 0;
	else if (s[i] == '"' && (*dq || *sq))
		*sq = 0;
}
