#include "shell.h"

int	sh_is_binary(char *arg)
{
	int		i;

	i = 0;
	while (arg[i])
	{
		if (arg[i] == '\'' || arg[i] == '"')
			return (0);
		if (i && arg[i] == '=' && arg[i + 1] && arg[i + 1] == '=' && arg[i + 2])
			return (i + 2);
		i++;
	}
	return (0);
}
